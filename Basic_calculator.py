def add(a,b):
    return a+b

def subtract(a,b):
    subtraction = a-b
    print('Subtraction of',a,'&',b,'=',subtraction)

def multiply(a,b):
    multiplication = a*b
    print('Multiplication of',a,'&',b,'=',multiplication)

def divide(a,b):
    division = a/b
    print('Division of',a,'&',b,'=',division)

result = add(3,2)
print(result)
subtract(3,2)
multiply(5,4)
divide(6,5)

